//ИМТ
function BMI()
{
    let input_height = Number(document.getElementById("height").value);
    let weight = Number(document.getElementById("weight").value);
    
    let height = input_height/100;
    let bmi = (weight/(Math.pow(height, 2))).toFixed(2);
    if (bmi <= 18.5) {document.getElementById("BmiResult").innerText = bmi + " | (Недостаточный вес)"; document.getElementById("BmiResult").style.color = "Lime";}
    else if (bmi <= 25) {document.getElementById("BmiResult").innerText = bmi + " (Нормально)"; document.getElementById("BmiResult").style.color = "green";}
    else if (bmi <= 30) {document.getElementById("BmiResult").innerText = bmi + " | (У вас излишек веса)"; document.getElementById("BmiResult").style.color = "OrangeRed";}
    else if (bmi > 30) {document.getElementById("BmiResult").innerText = bmi + " | (Ожирение)"; document.getElementById("BmiResult").style.color = "red";}
    
}

