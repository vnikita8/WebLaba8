//Сумма букв имени
function NameSum(name)
{
    let sum = 0;
    for (let i = 0; i<name.length; i++)
    {
        if (typeof sum == "number"){sum = sum + SymbolValue(name[i]);}
        else {
            sum = "Ошибка";
            break;
        }
    }
    let newSum = 0;
    sum = sum.toString();
    for (let i = 0; i<sum.toString().length; i++){newSum = newSum + Number(sum[i]);}
    while (newSum.toString().length != 1){
        let prom_sum = newSum.toString();
        newSum = 0;
        for (let i = 0; i<prom_sum.length; i++){newSum = newSum + Number(prom_sum[i]);}
    }
    return newSum;
}

